  static Future<void> init() async {
    final prefs = await SharedPreferences.getInstance();
    var saved = prefs.getString('app_lang') ?? 'ko';
    if (saved == 'ja') {
      saved = 'jp';
      await prefs.setString('app_lang', 'jp');
    }
    lang = AppLang.values.firstWhere(
      (e) => e.name == saved,
      orElse: () => AppLang.ko,
    );
  }

